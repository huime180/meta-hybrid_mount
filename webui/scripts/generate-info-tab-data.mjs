import { access, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const outputFile = path.join(rootDir, "src/lib/infoTabData.gen.ts");

const DETAIL_FETCH_LIMIT = 12;
const REPOSITORIES = [
  {
    id: "core",
    owner: "Hybrid-Mount",
    name: "meta-hybrid_mount",
    label: "Hybrid Mount",
  },
];

const apiHeaders = {
  Accept: "application/vnd.github+json",
  "User-Agent": "hybrid-mount-webui-info-generator",
  ...(process.env.GITHUB_TOKEN
    ? { Authorization: `Bearer ${process.env.GITHUB_TOKEN}` }
    : {}),
};

async function fetchJson(url) {
  const response = await fetch(url, { headers: apiHeaders });

  if (!response.ok) {
    throw new Error(
      `GitHub API request failed (${response.status} ${response.statusText}): ${url}`,
    );
  }

  return response.json();
}

function normalizeContributor(user) {
  return {
    login: user.login,
    avatar_url: user.avatar_url,
    html_url: user.html_url,
    type: user.type,
    url: user.url,
    name: user.login,
    bio: "",
  };
}

async function fetchRepositorySection(repo) {
  const contributorsUrl = `https://api.github.com/repos/${repo.owner}/${repo.name}/contributors`;
  const contributorList = await fetchJson(contributorsUrl);
  const filteredContributors = contributorList
    .filter((user) => {
      const isBotType = user.type === "Bot";
      const hasBotName = user.login.toLowerCase().includes("bot");
      return !isBotType && !hasBotName;
    })
    .map(normalizeContributor);

  const enrichedContributors = [...filteredContributors];
  const detailTargets = filteredContributors.slice(0, DETAIL_FETCH_LIMIT);
  const detailResults = await Promise.allSettled(
    detailTargets.map((user) => fetchJson(user.url)),
  );

  detailResults.forEach((result, index) => {
    if (result.status !== "fulfilled") {
      return;
    }

    const detail = result.value;
    enrichedContributors[index] = {
      ...enrichedContributors[index],
      name: detail.name || enrichedContributors[index].name,
      bio: detail.bio || enrichedContributors[index].bio,
    };
  });

  return {
    id: repo.id,
    label: repo.label,
    repoDisplayName: `${repo.owner}/${repo.name}`,
    repoUrl: `https://github.com/${repo.owner}/${repo.name}`,
    contributors: enrichedContributors,
  };
}

function buildOutput(sections) {
  const serializedSections = JSON.stringify(sections, null, 2);
  const generatedAt = new Date().toISOString();

  return `/**
 * Generated by scripts/generate-info-tab-data.mjs
 * Generated at: ${generatedAt}
 */

import type { InfoTabSection } from "./infoTabData";

export const INFO_TAB_SECTIONS: InfoTabSection[] = ${serializedSections};
`;
}

async function hasExistingOutput() {
  try {
    await access(outputFile);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  try {
    const sections = await Promise.all(
      REPOSITORIES.map((repo) => fetchRepositorySection(repo)),
    );

    await mkdir(path.dirname(outputFile), { recursive: true });
    await writeFile(outputFile, buildOutput(sections), "utf8");
    console.log(
      `Generated ${path.relative(rootDir, outputFile)} with ${sections.length} sections.`,
    );
  } catch (error) {
    if (await hasExistingOutput()) {
      console.warn(
        "Failed to refresh InfoTab data, keeping existing generated file.",
      );
      console.warn(error instanceof Error ? error.message : String(error));
      return;
    }

    throw error;
  }
}

await main();
